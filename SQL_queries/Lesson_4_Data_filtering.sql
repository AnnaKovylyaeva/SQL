-- 4.1.Напишите SQL-запрос к таблице products и выведите всю информацию о товарах, цена которых не превышает 100 рублей. 
-- Результат отсортируйте по возрастанию id товара.
-- Поля в результирующей таблице: product_id, name, price

SELECT product_id, name, price
FROM products
WHERE price <= 100
ORDER BY product_id;

-- 4.2. Отберите пользователей женского пола из таблицы users. Выведите только id этих пользователей.
-- Результат отсортируйте по возрастанию id. Добавьте в запрос оператор LIMIT и выведите только 1000 первых id из отсортированного списка.
-- Поле в результирующей таблице: user_id
 
SELECT user_id
FROM users
WHERE sex = 'female'
