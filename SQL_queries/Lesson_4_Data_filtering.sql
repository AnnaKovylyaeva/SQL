-- 4.3. Отберите из таблицы user_actions все действия пользователей по созданию заказов, 
-- которые были совершены ими после полуночи 6 сентября 2022 года.
-- Выведите колонки с id пользователей, id созданных заказов и временем их создания. Результат должен быть отсортирован по возрастанию id заказа.
