-- 4.13. Из таблицы user_actions получите id всех заказов, сделанных пользователями сервиса в августе 2022 года.
-- Результат отсортируйте по возрастанию id заказа.
-- Поле в результирующей таблице: order_id

SELECT order_id
FROM user_actions
WHERE DATE_PART('MONTH', time) = 8 AND DATE_PART('YEAR', time) = 2022 AND action = 'create_order'
ORDER BY order_id;

-- 4.14. Из таблицы couriers отберите id всех курьеров, родившихся в период с 1990 по 1995 год включительно.
-- Результат отсортируйте по возрастанию id курьера.
-- Поле в результирующей таблице: courier_id

SELECT courier_id
FROM couriers
WHERE DATE_PART('YEAR', birth_date) between 1990 AND 1995
ORDER BY courier_id;

-- 4.15. Из таблицы user_actions получите информацию о всех отменах заказов,
-- которые пользователи совершали в течение августа 2022 года по средам с 12:00 до 15:59.
-- Результат отсортируйте по убыванию id отменённых заказов.
-- Поля в результирующей таблице: user_id, order_id, action, time

SELECT user_id, order_id, action, time
FROM user_actions
WHERE action = 'cancel_order' 
      AND DATE_PART('MONTH', time) = 8
      AND DATE_PART('YEAR', time) = 2022
      AND DATE_PART('isodow', time) = 3
      AND (DATE_PART('HOUR', time) BETWEEN 12 AND 15)
      AND (DATE_PART('MINUTE', time) BETWEEN 0 AND 59)
ORDER BY order_id DESC;

-- 4.16. Как и в задаче из прошлого урока, вычислите НДС каждого товара в таблице products и рассчитайте цену без учёта НДС. 
-- Однако теперь примите во внимание, что для товаров из списка налог составляет 10%. Для остальных товаров НДС тот же — 20%.
-- Выведите всю информацию о товарах, включая сумму налога и цену без его учёта. 
-- Колонки с суммой налога и ценой без НДС назовите соответственно tax и price_before_tax. 
-- Округлите значения в этих колонках до двух знаков после запятой.
-- Результат отсортируйте сначала по убыванию цены товара без учёта НДС, затем по возрастанию id товара.
-- Поля в результирующей таблице: product_id, name, price, tax, price_before_tax

SELECT product_id,
       name,
       price,
       (CASE WHEN name IN ('сахар', 'сухарики', 'сушки', 'семечки', 
                         'масло льняное', 'виноград', 'масло оливковое', 
                         'арбуз', 'батон', 'йогурт', 'сливки', 'гречка', 
                         'овсянка', 'макароны', 'баранина', 'апельсины', 
                         'бублики', 'хлеб', 'горох', 'сметана', 'рыба копченая', 
                         'мука', 'шпроты', 'сосиски', 'свинина', 'рис', 
                         'масло кунжутное', 'сгущенка', 'ананас', 'говядина', 
                         'соль', 'рыба вяленая', 'масло подсолнечное', 'яблоки', 
                         'груши', 'лепешка', 'молоко', 'курица', 'лаваш', 'вафли', 'мандарины')
        THEN round(price/110*10, 2)
        ELSE round(price/120*20, 2)
        END) AS tax,
        (CASE WHEN name IN ('сахар', 'сухарики', 'сушки', 'семечки', 
                         'масло льняное', 'виноград', 'масло оливковое', 
                         'арбуз', 'батон', 'йогурт', 'сливки', 'гречка', 
                         'овсянка', 'макароны', 'баранина', 'апельсины', 
                         'бублики', 'хлеб', 'горох', 'сметана', 'рыба копченая', 
                         'мука', 'шпроты', 'сосиски', 'свинина', 'рис', 
                         'масло кунжутное', 'сгущенка', 'ананас', 'говядина', 
                         'соль', 'рыба вяленая', 'масло подсолнечное', 'яблоки', 
                         'груши', 'лепешка', 'молоко', 'курица', 'лаваш', 'вафли', 'мандарины')
        THEN round(price - price/110*10, 2)
        ELSE round(price - price/120*20, 2)
        END) AS price_before_tax
FROM products
ORDER BY price_before_tax desc, product_id;

