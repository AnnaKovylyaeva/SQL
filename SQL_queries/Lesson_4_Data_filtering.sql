-- 4.8. Составьте SQL-запрос, который выбирает из таблицы products все чаи стоимостью больше 60 рублей и вычисляет для них цену со скидкой 25%.
-- Скидку в % менеджер попросил указать в отдельном столбце в формате текста, то есть вот так: «25%» (без кавычек). 
-- Столбцы со скидкой и новой ценой назовите соответственно discount и new_price.
-- Также необходимо любым известным способом избавиться от «чайного гриба»: вряд ли менеджер имел в виду и его, когда ставил нам задачу.
-- Результат должен быть отсортирован по возрастанию id товара.
-- Поля в результирующей таблице: product_id, name, price, discount, new_price

SELECT product_id, name, price, '25%' AS discount, 0.75 * price AS new_price
FROM products
WHERE name LIKE '%чай%' AND name NOT LIKE 'чайный гриб' AND price > 60 
ORDER BY product_id

-- 4.9. Из таблицы user_actions выведите всю информацию о действиях пользователей с id 170, 200 и 230 
-- за период с 25 августа по 4 сентября 2022 года включительно. 
-- Результат отсортируйте по убыванию id заказа — то есть от самых поздних действий к самым первым.
-- Поля в результирующей таблице: user_id, order_id, action, time

SELECT user_id, order_id, action, time
FROM user_actions
WHERE user_id IN (170, 200, 230) AND (time >= '2022-08-25' AND time <= '2022-09-05')
ORDER BY order_id DESC; 

-- 4.10. Напишите SQL-запрос к таблице couriers и выведите всю информацию о курьерах, у которых не указан их день рождения.
-- Результат должен быть отсортирован по возрастанию id курьера.
