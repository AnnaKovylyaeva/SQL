SELECT user_id, order_id, time
FROM user_actions
WHERE time > '2022-09-06 00:00' AND action = 'create_order' 
ORDER BY order_id;

-- 4.4. Назначьте скидку 20% на все товары из таблицы products и отберите те, цена на которые с учётом скидки превышает 100 рублей. 
-- Выведите id товаров, их наименования, прежнюю цену и новую цену с учётом скидки. 
-- Колонку со старой ценой назовите old_price, с новой — new_price.
-- Результат должен быть отсортирован по возрастанию id товара.
-- Поля в результирующей таблице: product_id, name, old_price, new_price

SELECT product_id, name, price AS old_price, price - price *0.2 AS new_price
FROM products
WHERE price - price *0.2 >= 100
ORDER BY product_id;

-- 4.5. Отберите из таблицы products все товары, названия которых либо начинаются со слова «чай», либо состоят из пяти символов. 
-- Выведите две колонки: id товаров и их наименования.
-- Результат должен быть отсортирован по возрастанию id товара.
-- Поля в результирующей таблице: product_id, name

SELECT product_id, name
FROM products
WHERE SPLIT_PART(name, ' ', 1) = 'чай' OR LENGTH(name) = 5
ORDER BY product_id;

-- 4.6. Отберите из таблицы products все товары, содержащие в своём названии последовательность символов «чай» (без кавычек).
